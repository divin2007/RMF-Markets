interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>('en');

  useEffect(() => {
    const savedLang = localStorage.getItem('rmf_language') as Language;
    if (savedLang && (savedLang === 'en' || savedLang === 'fr' || savedLang === 'kin')) {
      setLanguage(savedLang);
    } else {
      const browserLang = typeof window !== 'undefined' ? navigator.language.split('-')[0] : 'en';
      if (browserLang === 'fr') setLanguage('fr');
      else if (browserLang === 'rw') setLanguage('kin');
    }
  }, []);

  const handleSetLanguage = (lang: Language) => {
    setLanguage(lang);
    localStorage.setItem('rmf_language', lang);
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    const translation = translations[language][key as keyof typeof translations['en']];
    const fallback = translations['en'][key as keyof typeof translations['en']];
    const value = String(translation || fallback || key);

    if (!params) return value;

    return Object.entries(params).reduce(
      (text, [paramKey, paramValue]) => text.replaceAll(`{${paramKey}}`, String(paramValue)),
      value,
    );
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage: handleSetLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
